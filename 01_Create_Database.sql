---------------------------------------------------------
-- Create Database
---------------------------------------------------------

CREATE DATABASE College;

---------------------------------------------------------
-- Use Database
---------------------------------------------------------

USE College;

---------------------------------------------------------
-- Create Student Table
---------------------------------------------------------

CREATE TABLE Student
(
    Id INT PRIMARY KEY,
    Name VARCHAR(50),
    Age INT NOT NULL
);

---------------------------------------------------------
-- Insert Records
---------------------------------------------------------

INSERT INTO Student (Id, Name, Age)
VALUES
(1, 'Vishal', 21),
(2, 'Ketan', 22);

---------------------------------------------------------
-- Display Records
---------------------------------------------------------

SELECT * FROM Student;

---------------------------------------------------------
-- Drop Table
---------------------------------------------------------

DROP TABLE Student;

